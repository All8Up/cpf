int BgfxIntegration::Start(const CommandLine&)
{
	SDL_Window* window = SDL_CreateWindow("Test", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, 1024, 768, 0);
	sdlSetWindow(window);
	bgfx::init(); // bgfx::RendererType::Direct3D11, BGFX_PCI_ID_NVIDIA);

	bgfx::reset(1024, 768, 0);
	bgfx::setDebug(BGFX_DEBUG_STATS);
	bgfx::setViewClear(
		0
		, BGFX_CLEAR_COLOR | BGFX_CLEAR_DEPTH
		, 0x303030ff
		, 1.0f
		, 0
	);


	bool running = true;
	SDL_Event sdlEvent;
	while (running)
	{
		while (SDL_PollEvent(&sdlEvent))
		{
			if (sdlEvent.type == SDL_QUIT)
			{
				running = false;
			}

			bgfx::setViewRect(0, 0, 0, uint16_t(1024), uint16_t(768));

			// This dummy draw call is here to make sure that view 0 is cleared
			// if no other draw calls are submitted to view 0.
			bgfx::touch(0);

			// Use debug font to print information about this example.
			bgfx::dbgTextClear();
			bgfx::dbgTextImage(bx::uint16_max(uint16_t(1024 / 2 / 8), 20) - 20
				, bx::uint16_max(uint16_t(768 / 2 / 16), 6) - 6
				, 40
				, 12
				, s_logo
				, 160
			);
			bgfx::dbgTextPrintf(0, 1, 0x4f, "bgfx/examples/00-helloworld");
			bgfx::dbgTextPrintf(0, 2, 0x6f, "Description: Initialization and debug text.");

			bgfx::dbgTextPrintf(0, 4, 0x0f, "Color can be changed with ANSI \x1b[9;me\x1b[10;ms\x1b[11;mc\x1b[12;ma\x1b[13;mp\x1b[14;me\x1b[0m code too.");

			const bgfx::Stats* stats = bgfx::getStats();
			bgfx::dbgTextPrintf(0, 6, 0x0f, "Backbuffer %dW x %dH in pixels, debug text %dW x %dH in characters."
				, stats->width
				, stats->height
				, stats->textWidth
				, stats->textHeight
			);

			// Advance to next frame. Rendering thread will be kicked to
			// process submitted rendering primitives.
			bgfx::frame();
		}
	}

	bgfx::shutdown();
	SDL_DestroyWindow(window);
	return 0;
}


