namespace CPF
{
	//////////////////////////////////////////////////////////////////////////
	template <typename LHS, typename RHS>
	LHS Min(LHS a, RHS b)
	{
		return LHS((a < b) ? a : b);
	}

	template <typename LHS, typename RHS>
	LHS Max(LHS a, RHS b)
	{
		return LHS((a > b) ? a : b);
	}

	template <typename TYPE>
	TYPE Abs(TYPE value)
	{
		return value >= 0 ? value : -value;
	}
}
